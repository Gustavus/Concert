<!-- InstanceBeginEditable name="Content" -->
<p>The Office of the Provost is offering support to faculty for the purpose of deepening the global content of academic courses or departmental curricula (as identified in the <a href="../strategicdirections/"><em>Academic Strategic Plan</em></a>, April 2010) through Globalizing the Curriculum Grants.</p>
<p>Grants are competitive and available to both individual faculty members as well as academic departments and programs. Whereas all faculty are invited to submit proposals, priority will be given to applications submitted by tenured, tenure-track, or continuing adjunct faculty because of the long-term benefit their initiatives will afford the Gustavus curriculum. Note that Globalizing the Curriculum Grants are available for January interim courses or one-time special topics courses in the curriculum, but priority will be given to regular, semester-long catalog courses that will be offered a minimum of three times.</p>
<p>In accepting funds, awardees agree to share their course syllabus with the Provost’s Office and campus community as appropriate. Contact Provost Mark Braun (<a href="mailto:mbraun@gustavus.edu">mbraun@gustavus.edu</a> or x7541) with any questions.</p>
<h2>Application</h2>
<p>Please complete the  <a href="https://gustavus.edu/provost/awards/documents/GlobalizingtheCurriculumGrantApplication2014.docx">grant application and budget information form</a>.</p>
<p>Funding priority will be assigned for proposals that clearly address each of the following criteria:</p>
<ul>
  <li>  	Articulate the educational value of the new/redesigned course as a means of targeting a goal and benchmark from the “Global and Multicultural Curriculum and Learning Environments” section of the <a href="../strategicdirections/"><em>Academic Strategic Plan</em></a>.</li>
  <li>Outline a sound plan for developing/redesigning the target course. Explain what will be done, when it will be completed and the anticipated outcome of each action.</li>
  <li>Address the curricular impact the target course will have in terms of frequency offered and enrollment.</li>
  <li>Describe a well-conceived plan for assessing the impact the target course has on student learning.</li>
  <li>Discuss appropriate materials and activities necessary to develop/redesign the course.</li>
</ul>
<p>Proposals must also include a letter from the relevant department chair/program director confirming the department’s commitment to offering the course (when and how frequently it will be offered) and endorsing the proposed course changes.</p>
<h2>Budget</h2>
<ul>
  <li>  Globalizing the Curriculum Grants will be awarded for up to $2,000, which may be divided among several worthy applications.</li>
  <li>Funds may be used to acquire materials and supplies, reimburse expenses and fees for teaching seminars or workshops directly related to the course content; for visiting archives or collections necessary to develop or significantly redesign an existing course; and to pay printing or photocopying costs.</li>
  <li>All reimbursements for mileage are capped at a maximum of $350.</li>
  <li>The funds cannot be used for stipends for Gustavus Adolphus College faculty or students.</li>
  <li>Grant money may not be used for workshops, seminars, or meetings for which participants are given a stipend by an outside granting agency, or to present or publish research, scholarship, or creative works.</li>
  <li>Faculty will have one fiscal year to spend the grant: 1 June 2015 through 31 May 2016</li>
</ul>
<h2>Submission</h2>
<p>  Applications are due by 16 February 2015. Funding decisions will be made by a group composed of the Provost, Kendall Center Director, Director of the Center for International and Cultural Education, Chair of the Diversity, Equity, and Inclusion Committee (or designee from the DEIC), and the Chair of the International and Domestic Programs Committee (or designee from the IDPC) with additional or substitute reviewers included at the discretion of the Provost. Decisions will be announced by mid-March 2015.</p>
<h2>Final Report</h2>
<p>  Submission of a final report addressing the outcomes of the activities undertaken will be due 30 September 2016, to Provost Mark Braun (send via email to <a href="mailto:snowell@gustavus.edu">snowell@gustavus.edu</a>). If applicable, please provide the course syllabus as an attachment to the report. Applicants failing to submit final reports will not be considered for additional internally-funded grants.<br />
</p>
